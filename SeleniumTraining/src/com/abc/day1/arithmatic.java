package com.abc.day1;

public class arithmatic {
	
	public  int addNumbers(int num1, int num2)
	{
		int sum = num1+num2;
		return sum;
	}
	
	public int subtractNumbers(int num1, int num2)
	{
		int sub = num1-num2;
		return sub;
	}
	
	public int multiplyNumbers(int num1, int num2)
	{
		int mul = num1*num2;
		return mul;
	}
}
